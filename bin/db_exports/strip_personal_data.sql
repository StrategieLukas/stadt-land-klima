CREATE TEMP TABLE temp_protected_user_emails AS
SELECT lower(email) AS email
FROM directus_users
WHERE email IS NOT NULL
  AND (
    coalesce(first_name, '') ~* '(FRONTEND|CLI|DIRECTUS)'
    OR coalesce(last_name, '') ~* '(FRONTEND|CLI|DIRECTUS)'
  );

CREATE TEMP TABLE temp_anon_name_pool AS
SELECT
  row_number() OVER (ORDER BY l.name_index, f.name_index) AS name_index,
  f.first_name,
  l.last_name,
  lower(f.first_name || '.' || l.last_name || '@stadt-land-klima.de') AS email
FROM temp_anon_first_names f
CROSS JOIN temp_anon_last_names l
WHERE lower(f.first_name || '.' || l.last_name || '@stadt-land-klima.de') NOT IN (
  SELECT email FROM temp_protected_user_emails
);

CREATE TEMP TABLE temp_users_to_replace AS
SELECT
  u.id,
  row_number() OVER (ORDER BY u.id) AS replacement_index
FROM directus_users u
WHERE NOT (
  coalesce(u.first_name, '') ~* '(FRONTEND|CLI|DIRECTUS)'
  OR coalesce(u.last_name, '') ~* '(FRONTEND|CLI|DIRECTUS)'
);

DO $$
DECLARE
  required_names integer;
  available_names integer;
BEGIN
  SELECT count(*) INTO required_names FROM temp_users_to_replace;
  SELECT count(*) INTO available_names FROM temp_anon_name_pool;

  IF required_names > available_names THEN
    RAISE EXCEPTION 'Not enough anonymized names: required %, available %',
      required_names,
      available_names;
  END IF;
END $$;

CREATE TEMP TABLE temp_user_email_replacements AS
SELECT
  u.id AS user_id,
  u.email AS original_email,
  false AS is_protected,
  p.first_name AS new_first_name,
  p.last_name AS new_last_name,
  p.email AS new_email
FROM directus_users u
JOIN temp_users_to_replace r ON r.id = u.id
JOIN temp_anon_name_pool p ON p.name_index = r.replacement_index
UNION ALL
SELECT
  u.id AS user_id,
  u.email AS original_email,
  true AS is_protected,
  u.first_name AS new_first_name,
  u.last_name AS new_last_name,
  u.email AS new_email
FROM directus_users u
WHERE (
  coalesce(u.first_name, '') ~* '(FRONTEND|CLI|DIRECTUS)'
  OR coalesce(u.last_name, '') ~* '(FRONTEND|CLI|DIRECTUS)'
);

UPDATE directus_users u
SET
  first_name = r.new_first_name,
  last_name = r.new_last_name,
  email = r.new_email,
  location = CASE WHEN r.is_protected THEN u.location ELSE NULL END,
  title = CASE WHEN r.is_protected THEN u.title ELSE NULL END,
  description = CASE WHEN r.is_protected THEN u.description ELSE NULL END,
  tags = CASE WHEN r.is_protected THEN u.tags ELSE NULL END,
  bio = CASE WHEN r.is_protected THEN u.bio ELSE NULL END,
  auth_data = CASE WHEN r.is_protected THEN u.auth_data ELSE NULL END,
  external_identifier = CASE WHEN r.is_protected THEN u.external_identifier ELSE NULL END,
  tfa_secret = CASE WHEN r.is_protected THEN u.tfa_secret ELSE NULL END,
  token = CASE WHEN r.is_protected THEN u.token ELSE NULL END
FROM temp_user_email_replacements r
WHERE u.id = r.user_id;

CREATE TEMP TABLE temp_editor_matched_emails AS
SELECT
  e.id AS editor_id,
  min(r.new_email) AS new_email
FROM editors e
JOIN temp_user_email_replacements r
  ON e.email IS NOT NULL
  AND r.original_email IS NOT NULL
  AND lower(e.email) = lower(r.original_email)
GROUP BY e.id;

CREATE TEMP TABLE temp_editor_initial_unmatched AS
SELECT e.id
FROM editors e
LEFT JOIN temp_editor_matched_emails m ON m.editor_id = e.id
WHERE m.editor_id IS NULL;

CREATE TEMP TABLE temp_editor_single_related_emails AS
WITH related_users AS (
  SELECT
    e.id AS editor_id,
    r.new_email,
    count(*) OVER (PARTITION BY e.id) AS related_user_count
  FROM temp_editor_initial_unmatched e
  JOIN editors original_editor ON original_editor.id = e.id
  JOIN junction_directus_users_localteams j
    ON j.localteam_id = original_editor.localteam_id
  JOIN temp_user_email_replacements r
    ON r.user_id = j.directus_users_id
),
deduplicated_related_users AS (
  SELECT
    editor_id,
    new_email,
    row_number() OVER (PARTITION BY lower(new_email) ORDER BY editor_id) AS email_use_index
  FROM related_users
  WHERE related_user_count = 1
    AND NOT EXISTS (
      SELECT 1
      FROM temp_editor_matched_emails matched
      WHERE lower(matched.new_email) = lower(related_users.new_email)
    )
)
SELECT
  editor_id,
  new_email
FROM deduplicated_related_users
WHERE email_use_index = 1;

CREATE TEMP TABLE temp_editor_unmatched AS
SELECT
  e.id,
  row_number() OVER (ORDER BY e.id) AS replacement_index
FROM temp_editor_initial_unmatched e
LEFT JOIN temp_editor_single_related_emails s ON s.editor_id = e.id
WHERE s.editor_id IS NULL;

DO $$
DECLARE
  used_user_names integer;
  required_editor_names integer;
  available_names integer;
BEGIN
  SELECT count(*) INTO used_user_names FROM temp_users_to_replace;
  SELECT count(*) INTO required_editor_names FROM temp_editor_unmatched;
  SELECT count(*) INTO available_names FROM temp_anon_name_pool;

  IF used_user_names + required_editor_names > available_names THEN
    RAISE EXCEPTION 'Not enough anonymized names for unmatched editors: required %, available %',
      used_user_names + required_editor_names,
      available_names;
  END IF;
END $$;

CREATE TEMP TABLE temp_editor_fallback_emails AS
SELECT
  e.id AS editor_id,
  p.email AS new_email
FROM temp_editor_unmatched e
JOIN temp_anon_name_pool p
  ON p.name_index = e.replacement_index + (SELECT count(*) FROM temp_users_to_replace);

UPDATE editors e
SET
  email = coalesce(m.new_email, s.new_email, f.new_email),
  internal_comment = NULL
FROM temp_editor_matched_emails m
FULL JOIN temp_editor_single_related_emails s ON s.editor_id = m.editor_id
FULL JOIN temp_editor_fallback_emails f ON f.editor_id = coalesce(m.editor_id, s.editor_id)
WHERE e.id = coalesce(m.editor_id, s.editor_id, f.editor_id);

CREATE TEMP TABLE temp_candidate_replacements AS
SELECT
  c.id,
  p.first_name,
  p.last_name,
  p.email
FROM (
  SELECT
    id,
    row_number() OVER (ORDER BY id) AS replacement_index
  FROM candidate
) c
JOIN temp_anon_name_pool p
  ON p.name_index = c.replacement_index
    + (SELECT count(*) FROM temp_users_to_replace)
    + (SELECT count(*) FROM temp_editor_unmatched);

